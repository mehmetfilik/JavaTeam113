package day04_dersTekrari;

public class C05 {

    public static void main(String[] args) {

        int sayi = 10;

        sayi *= 3;

        System.out.println(sayi); // 30

        sayi += 3;

        System.out.println(sayi); // 33

        sayi -= 3;

        System.out.println(sayi); // 30

        sayi /= 3;

        System.out.println(sayi); // 10


    }

}
